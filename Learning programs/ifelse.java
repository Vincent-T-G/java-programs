public class ifelse 
{
    public static void main(String args[])
    {
        int x = 8;

        if (x > 10 && x <= 20)
            System.out.println("Hello!");
        else
            System.out.println("Bye!");

        int a = 7;
        int b = 9;

        if (a > b)
        {
            System.out.print(a);
            System.out.print(" is greater than 9");
        }
        else
        {
            System.out.print(b);
            System.out.print(" is greater than 7");
        }
    }    
}
